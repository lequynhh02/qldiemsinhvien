    public void combo() {
        try {
            Connection connection = KetNoi.getConnection();
            PreparedStatement TKKhoa = connection.prepareStatement("SELECT makhoa FROM khoa group by makhoa ");
            PreparedStatement CBmakhoa = connection.prepareStatement("SELECT makhoa FROM khoa group by makhoa ");
            PreparedStatement CBmanganh = connection.prepareStatement("SELECT manganh FROM chuyennganh group by manganh ");

            ResultSet rs1 = TKKhoa.executeQuery();
            ResultSet rs2 = CBmakhoa.executeQuery();
            ResultSet rs3 = CBmanganh.executeQuery();
            //combo khóa tìm
            while (rs1.next()) {
                cboKhoaTim.addItem(rs1.getString("makhoa"));
            }//combo mã khóa
            while (rs2.next()) {
                cboKhoa.addItem(rs2.getString("makhoa"));
            }//combo mã ngành
            while (rs3.next()) {
                cboNganh.addItem(rs3.getString("manganh"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
