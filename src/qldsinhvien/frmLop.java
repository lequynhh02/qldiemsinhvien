    private void cmdLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLuuActionPerformed
        if (txtMaLop.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Mã lớp không được rỗng");
            txtMaLop.requestFocus();
            return;
        }
        if (txtTenLop.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Tên lớp không được rỗng");
            txtTenLop.requestFocus();
            return;
        }

        if (ktThem == true) {
            try {
                if (ktTrungThem(txtMaLop.getText()) == true) {
                    JOptionPane.showMessageDialog(null, "Trùng mã thêm: " + txtMaLop.getText().trim() + " !"/*, "Error", JOptionPane.ERROR_MESSAGE*/);
                    return;
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        try {
            Connection conn = KetNoi.getConnection();
            if (ktThem == true) {
                sql = "insert into lop (malop, makhoa, manganh, tenlop, GVCN, SDT) "
                        + "values(N'" + txtMaLop.getText() + "', N'" + cboKhoa.getSelectedItem().toString() + "', N'" 
                        + cboNganh.getSelectedItem().toString() + "', N'" + txtTenLop.getText() + "', N'" 
                        + txtGVCN.getText() + "', N'" + txtSDT.getText() + "')";
            } else {
                sql = "update lop set malop= N'" + txtMaLop.getText() +"', makhoa= N'" + cboKhoa.getSelectedItem().toString()
                        +"', manganh= N'" + cboNganh.getSelectedItem().toString() + "', tenlop= N'" + txtTenLop.getText() 
                        + "', GVCN= N'" + txtGVCN.getText() + "', SDT= N'" + txtSDT.getText() + "' "
                        + "where malop= N'" + macu + "'";
            }
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.executeUpdate();
            KetNoi.closeConnection(conn);

            if (ktThem == true) {
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Sửa thành công");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        KhoaMo(false);
        showdata();
    }//GEN-LAST:event_cmdLuuActionPerformed

