    private void cboKhoaTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboKhoaTimActionPerformed
        try {
            timkiem = cboKhoaTim.getSelectedItem().toString();
            tblLop.removeAll();
            String[] arr = {"Mã Lớp", "Tên Lớp", "Khóa", "Ngành", "GVCN", "SĐT"};
            DefaultTableModel model = new DefaultTableModel(arr, 0); //tạo TableModel chứa dữ liệu cho Jtable
            String query = "select * from Lop where MaKhoa = '" + timkiem + "'";

            Connection connection = KetNoi.getConnection();

            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            // hiển thị dữ liệu lên jtable
            while (rs.next()) {
                Vector vector = new Vector(); // khỏi tạo 1 vector
                vector.add(rs.getString("MALOP"));
                vector.add(rs.getString("TENLOP"));
                vector.add(rs.getString("MANGANH"));
                vector.add(rs.getString("MAKHOA"));
                vector.add(rs.getString("GVCN"));
                vector.add(rs.getString("SDT"));

                model.addRow(vector);

            }
            tblLop.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_cboKhoaTimActionPerformed
