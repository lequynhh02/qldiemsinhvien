    private void showdata() {
        try {
            tblChuyenNganh.removeAll();
            String[] arr = {"Mã Ngành", "Tên Ngành"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);

            Connection connection = KetNoi.getConnection();
            String query = "SELECT * FROM dbo.[CHUYENNGANH]";
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("MaNganh"));
                vector.add(rs.getString("TenNganh"));

                model.addRow(vector);
            }
            tblChuyenNganh.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(frmChuyenNganh.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmdLuu.setEnabled(false);

    }

