    private void cmdLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLuuActionPerformed
        if (txtMaNganh.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Mã ngành không được rỗng");
            txtMaNganh.requestFocus();
            return;
        }
        if (txtTenNganh.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Tên ngành không được rỗng");
            txtTenNganh.requestFocus();
            return;
        }
        if (ktThem == true) {
            try {
                if (ktTrungThem(txtMaNganh.getText()) == true) {
                    JOptionPane.showMessageDialog(null, "Trùng mã thêm: " + txtMaNganh.getText().trim() + " !");
                    return;
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        try {
            Connection conn = KetNoi.getConnection();
            if (ktThem == true) {
                sql = "insert into ChuyenNganh(MaNganh,TenNganh) "
                        + "values(N'" + txtMaNganh.getText() + "',N'" + txtTenNganh.getText() + "')";
            } else {
                sql = "update ChuyenNganh set TenNganh= N'" + txtTenNganh.getText() + "' "
                        + "where MaNganh= N'" + macu + "'";
            }           
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.executeUpdate();
            KetNoi.closeConnection(conn);
            
            if (ktThem == true) {
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Sửa thành công");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        KhoaMo(false);
        showdata();
    }//GEN-LAST:event_cmdLuuActionPerformed

